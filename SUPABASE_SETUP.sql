-- JC Handyman Cloud CRM V16 Stable Foundation
-- Run in Supabase SQL Editor.

create table if not exists public.crm_upgrades (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  version text,
  upgrade_name text,
  notes text,
  status text
);

create table if not exists public.materials (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  name text,
  quantity numeric,
  low_stock numeric,
  cost numeric,
  supplier text,
  notes text
);

create table if not exists public.work_orders (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  customer_id bigint,
  job_id bigint,
  title text,
  status text,
  scope text,
  due_date date
);

alter table public.crm_upgrades enable row level security;
alter table public.materials enable row level security;
alter table public.work_orders enable row level security;

drop policy if exists "Allow public select crm_upgrades" on public.crm_upgrades;
drop policy if exists "Allow public insert crm_upgrades" on public.crm_upgrades;
drop policy if exists "Allow public select materials" on public.materials;
drop policy if exists "Allow public insert materials" on public.materials;
drop policy if exists "Allow public select work_orders" on public.work_orders;
drop policy if exists "Allow public insert work_orders" on public.work_orders;

create policy "Allow public select crm_upgrades" on public.crm_upgrades for select using (true);
create policy "Allow public insert crm_upgrades" on public.crm_upgrades for insert with check (true);
create policy "Allow public select materials" on public.materials for select using (true);
create policy "Allow public insert materials" on public.materials for insert with check (true);
create policy "Allow public select work_orders" on public.work_orders for select using (true);
create policy "Allow public insert work_orders" on public.work_orders for insert with check (true);

insert into public.crm_upgrades (version, upgrade_name, notes, status)
values ('V16', 'Stable Foundation', 'Integrated upgrade manager and stable CRM foundation.', 'Installed');
