insert into storage.buckets (id, name, public)
values ('job-files', 'job-files', true)
on conflict (id) do update set public = true;

drop policy if exists "Allow public read job files" on storage.objects;
drop policy if exists "Allow public upload job files" on storage.objects;

create policy "Allow public read job files"
on storage.objects
for select
using (bucket_id = 'job-files');

create policy "Allow public upload job files"
on storage.objects
for insert
with check (bucket_id = 'job-files');

create table if not exists public.signatures (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  customer_id bigint,
  title text,
  signature_url text
);

alter table public.signatures enable row level security;

drop policy if exists "Allow public select signatures" on public.signatures;
drop policy if exists "Allow public insert signatures" on public.signatures;

create policy "Allow public select signatures"
on public.signatures
for select
using (true);

create policy "Allow public insert signatures"
on public.signatures
for insert
with check (true);
