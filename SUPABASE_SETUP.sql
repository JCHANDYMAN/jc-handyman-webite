create table if not exists public.customers (id bigint generated by default as identity primary key, created_at timestamptz not null default now(), name text, phone text, email text, address text, property_type text, membership_plan text, notes text);
create table if not exists public.jobs (id bigint generated by default as identity primary key, created_at timestamptz not null default now(), customer_id bigint, job_type text, status text, description text, estimate numeric, invoice numeric, appointment date, notes text);
create table if not exists public.website_leads (id bigint generated by default as identity primary key, created_at timestamptz not null default now(), name text, phone text, email text, address text, service text, message text, status text);
alter table public.customers enable row level security;
alter table public.jobs enable row level security;
alter table public.website_leads enable row level security;
drop policy if exists "public read customers" on public.customers;
drop policy if exists "public insert customers" on public.customers;
drop policy if exists "public read jobs" on public.jobs;
drop policy if exists "public insert jobs" on public.jobs;
drop policy if exists "public read leads" on public.website_leads;
drop policy if exists "public insert leads" on public.website_leads;
