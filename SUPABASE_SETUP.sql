-- JC Handyman Cloud CRM V10 Photos, Documents, Payments

create table if not exists public.job_photos (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  customer_id bigint,
  job_id bigint,
  photo_type text,
  photo_url text,
  notes text
);

create table if not exists public.documents (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  customer_id bigint,
  job_id bigint,
  document_type text,
  title text,
  document_url text,
  notes text
);

create table if not exists public.payments (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  customer_id bigint,
  invoice_id bigint,
  amount numeric,
  method text,
  payment_date date,
  notes text
);

alter table public.job_photos enable row level security;
alter table public.documents enable row level security;
alter table public.payments enable row level security;

drop policy if exists "Allow public select job_photos" on public.job_photos;
drop policy if exists "Allow public insert job_photos" on public.job_photos;
drop policy if exists "Allow public select documents" on public.documents;
drop policy if exists "Allow public insert documents" on public.documents;
drop policy if exists "Allow public select payments" on public.payments;
drop policy if exists "Allow public insert payments" on public.payments;

create policy "Allow public select job_photos" on public.job_photos for select using (true);
create policy "Allow public insert job_photos" on public.job_photos for insert with check (true);
create policy "Allow public select documents" on public.documents for select using (true);
create policy "Allow public insert documents" on public.documents for insert with check (true);
create policy "Allow public select payments" on public.payments for select using (true);
create policy "Allow public insert payments" on public.payments for insert with check (true);
