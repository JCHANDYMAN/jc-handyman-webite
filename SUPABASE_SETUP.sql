create policy "public insert customers" on public.customers for insert with check (true);
create policy "public read jobs" on public.jobs for select using (true);
create policy "public insert jobs" on public.jobs for insert with check (true);
create policy "public read leads" on public.website_leads for select using (true);
create policy "public insert leads" on public.website_leads for insert with check (true);
